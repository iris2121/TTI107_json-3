// 1.4 Comparação
// não use ==, apenas ===
console.log(1 == 1)//true
console.log (1 == "1") //true
console.log (1 === 1) //true
console.log (1 === "1")//false
console.log (true == 1) //true
console.log (1 == [1])//true
console.log (null == null)//true
console.log (null == undefined)//true
console.log([] == false)//true
console.log ([] == [])//false